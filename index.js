function toggleTheme() {
    document.body.classList.toggle('dark-mode');
    document.body.classList.toggle('light-mode');
    updateIcon();
}

function changeLanguage(lang, event) {
    // Grundlegende Texte
    document.getElementById('about-title').innerText = translations[lang].aboutTitle;
    document.getElementById('about-text').innerText = translations[lang].aboutText;
    document.getElementById('offer-title').innerText = translations[lang].offerTitle;
    document.getElementById('esculta-title').innerText = translations[lang].escultaTitle;
    document.getElementById('esculta-text').innerText = translations[lang].escultaText;
    document.getElementById('ciclos-title').innerText = translations[lang].ciclosTitle;
    document.getElementById('ciclos-text').innerText = translations[lang].ciclosText;
    document.getElementById('liberdade-title').innerText = translations[lang].liberdadeTitle;
    document.getElementById('liberdade-text').innerText = translations[lang].liberdadeText;
    document.getElementById('rates-title').innerText = translations[lang].ratesTitle;
    
    // Preise
    document.getElementById('rate-eu').innerText = translations[lang].rateeu;
    document.getElementById('rate-ao').innerText = translations[lang].rateao;
    document.getElementById('rate-mz').innerText = translations[lang].ratemz;
    
    // "/ Session" Labels (Korrektur der Variablen-Tippfehler)
    const sessionLabels = document.querySelectorAll('.per-session-label');
    sessionLabels.forEach(label => {
        label.innerText = translations[lang].persessionlabel;
    });

    // Buttons & Footer
    document.getElementById('booking-btn').innerText = translations[lang].booking;
    document.getElementById('social-label').innerText = translations[lang].social;
    document.getElementById('privacy-trigger').innerText = translations[lang].privacyLink;
    document.getElementById('impressum-title').innerText = translations[lang].impressumTitle;
    document.getElementById('impressum-content').innerText = translations[lang].impressumContent;
    document.getElementById('policy-subtitle').innerText = translations[lang].policySubtitle;
    document.getElementById('policy-text').innerText = translations[lang].policyText;
    document.getElementById('cookie-subtitle').innerText = translations[lang].cookieSubtitle;
    document.getElementById('cookie-text').innerText = translations[lang].cookieText;

    // Nav UI (Bold/Underline)
    document.querySelectorAll('.languages span').forEach(s => s.classList.remove('active'));
    if (event) {
        event.target.classList.add('active');
    }
}

// Initialisierung
window.onload = function() {
    updateIcon();
};

function openModal() {
    document.getElementById("privacy-modal").style.display = "block";
}

function closeModal() {
    document.getElementById("privacy-modal").style.display = "none";
}

window.onclick = function(event) {
    let modal = document.getElementById("privacy-modal");
    if (event.target == modal) {
        closeModal();
    }
};